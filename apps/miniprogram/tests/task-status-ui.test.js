test('自定义播放器使用单一时间、可拖动进度和本地图标', () => {
	assert.ok(fs.existsSync('components/audio-player/index.js'))
	assert.ok(fs.existsSync('components/audio-player/index.wxml'))
	assert.ok(fs.existsSync('components/audio-player/index.wxss'))
	const config = JSON.parse(read('components/audio-player/index.json'))
	const component = read('components/audio-player/index.wxml')
	const script = read('components/audio-player/index.js')
	assert.equal(config.component, true)
	assert.match(component, /assets\/icons\/iconfont\/play\.svg/)
	assert.match(component, /assets\/icons\/iconfont\/pause\.svg/)
	assert.match(component, /<slider[^>]*bindchange="seek"/)
	assert.equal((component.match(/{{timeText}}/g) || []).length, 1)
	assert.match(script, /createAudioPlayback/)
	assert.match(script, /createInnerAudioContext/)
	assert.match(script, /triggerEvent\('error'/)
	assert.match(script, /onState:\s*\(\{\s*playing,\s*progress,\s*timeText\s*\}\)\s*=>\s*this\.setData\(\{\s*playing,\s*progress,\s*timeText\s*\}\)/)
	assert.doesNotMatch(script, /this\.setData\(state\)/)
})
